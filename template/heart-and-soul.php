								<ul>
									<li><a href="http://unsplash.com/" target="_blank">unsplash.com</a> - very cool free stock photos - 10 new photos added every day</li>
									<li><a href="http://deathtothestockphoto.com/" target="_blank">deathtothestockphoto.com</a> - sign up to receive a free stock photo pack every month</li>
									<li><a href="http://kaboompics.com/" target="_blank">kaboompics.com</a> - hundreds of free high-res images with good variety</li>
									<li><a href="http://nos.twnsnd.co/" target="_blank">nos.twnsnd.co</a> - curated collection of free, public domain vintage photos</li>
									<li><a href="http://startupstockphotos.com/" target="_blank">startupstockphotos.com</a> - great free ‘techy’ photos--would be best for a corporate website.</li>
									<li><a href="http://www.creativemarket.com/" target="_blank">creativemarket.com</a> - large selection of affordable stock and textures you can use in the .psds in step 08 below.</li>
								</ul>
							</p>
							<p><br>Once you have photos chosen to use on each page, you will need to make sure they’re in the right format for Squarespace. Some photos may need to be resized in Photoshop to ensure that they look their best on your site. Here’s a checklist to use when prepping your photos for Squarespace. Photos should be:
							</p>
							<p><br>
								<ul>
									<li><strong>In .jpg, .gif. or .png format only.</strong> We recommend saving photos in .jpg format and logos, icons or other graphics that might need a transparent background as a .png</li>
									<li><strong>sized at 1500px wide.</strong> The one exception is if you’re using a photo for a responsive hero image or background image—those photos should be saved at 2500px wide.</li>
									<li><strong>in RGB color mode (not CMYK).</strong> RGB is the color mode used for images on web. CMYK is for printed materials.</li>
									<li><strong>in a sRGB color profile.</strong> Most images have this already, so no need to worry about it. However if after uploading your images to Squarespace, the colors look off, it’s worth opening the images in Photoshop to make sure they have the correct sRGB color profile.</li>
									<li>less than 500 KB in file size. Squarespace can accommodate uploads up to 20 MB, but 500 KB will give you best results and load times.</li>
								</ul>
							</p>
							<p><br><strong>TIP:</strong> If after adding photos to your site, your images or graphics look ‘fuzzy’ or ‘pixelated,’ try loading your site on different computers and different browsers. We’ve noticed sometimes images can look fuzzy in out-of-date browsers or at certain browser sizes, but it’s an issue with the browser—not with your images. Before you go resizing or reuploading your images to fix the problem, make sure it’s not just a browser issue.
